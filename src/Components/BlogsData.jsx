export const getBlogBySlug = (slug) => {
  return blogsData.find(blog => blog.slug === slug);
};

export const getBlogById = (id) => {
  return blogsData.find(blog => blog.id === parseInt(id));
};

export const getRelatedBlogs = (currentBlog, limit = 3) => {
  return blogsData
    .filter(blog => blog.id !== currentBlog.id && blog.category === currentBlog.category)
    .slice(0, limit);
};

export const getPopularBlogs = (limit = 5) => {
  return blogsData
    .sort((a, b) => b.views - a.views)
    .slice(0, limit);
};

export const getRecentBlogs = (limit = 5) => {
  return blogsData
    .sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt))
    .slice(0, limit);
};